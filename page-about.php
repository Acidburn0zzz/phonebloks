		<h1>How we want to achieve this</h1>

		<p>We at Phonebloks see ourselves as a means to promote a holistic approach to end or reduce the various ethical and environmental problems existing in the consumer electronic market today. We believe in transformation from, a flat to a circular economy, as well as steering towards new production methods and indeed new products. Logistic solutions and production materials will help us reach a more sensible industry. We see this transformation creating less waste and pollution as well as a longer life-cycle and higher lever of recyclability (or even bio-degradability) for the end product. We advocate transparency, open source and open innovation and want to be the hub where the industry talk and listen to each other and to the public.</p>

		<p>The first, and perhaps biggest step for us, is to aid the existing industry in steering away from manufacturing products that are sold and repaired or replaced as whole-widgets, to products that are modular. And in that, also easy to repair or upgrade without the need for whole widget replacement. This will increase the life-span of the product, at the same time as reduce waste.</p>

